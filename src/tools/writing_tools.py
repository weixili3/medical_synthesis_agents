@tool
def create_plotly_chart(chart_spec_json: str) -> str:
    """
    Dynamically generate a Plotly chart from a flexible chart specification.

    Supports multiple chart types and multi-series data. Returns the Plotly figure
    as JSON (always) and a PNG base64 data URI (if the kaleido package is installed).

    Args:
        chart_spec_json: JSON string with:
            - chart_type (str): One of "bar" | "line" | "scatter" | "pie" |
              "heatmap" | "box" | "area" | "waterfall". Default "bar".
            - title (str): Chart title.
            - x_label (str, optional): X-axis label.
            - y_label (str, optional): Y-axis label.
            - data (list[dict]): One dict per series/trace. Supported keys per
              series vary by chart_type:
                • bar / line / scatter / area:
                    x (list), y (list), name (str),
                    error_y (dict, optional) — e.g. {"array": [0.1, 0.2]}
                • pie:
                    labels (list), values (list), name (str),
                    hole (float 0-1, optional) — 0 = pie, >0 = donut
                • heatmap:
                    x (list), y (list), z (list[list]),
                    colorscale (str, optional, default "Blues")
                • box:
                    y (list), name (str),
                    boxpoints (str, optional) — "outliers" | "all" | False
                • waterfall:
                    x (list), y (list), name (str),
                    measure (list, optional) — "relative" | "total" | "absolute"
            - layout (dict, optional): Any Plotly layout overrides
              (e.g. {"barmode": "group", "showlegend": true}).
            - orientation (str, optional): "v" (default) or "h" for bar charts.

        Example — grouped bar chart:
            {
              "chart_type": "bar",
              "title": "HbA1c Reduction by Intervention",
              "x_label": "Intervention",
              "y_label": "HbA1c Change (%)",
              "data": [
                {"x": ["Diet", "Exercise", "Combined"], "y": [-0.5, -0.7, -1.2],
                 "name": "6-month", "error_y": {"array": [0.1, 0.15, 0.2]}},
                {"x": ["Diet", "Exercise", "Combined"], "y": [-0.8, -1.0, -1.5],
                 "name": "12-month"}
              ],
              "layout": {"barmode": "group"}
            }

    Returns:
        JSON string with:
            - "plotly_json" (str): Full Plotly figure JSON for interactive embedding.
            - "png_base64" (str, optional): data:image/png;base64,... URI for
              static markdown embedding (only present when kaleido is installed).
        On error returns a plain error string.
    """
    try:
        import plotly.graph_objects as go
        import plotly.io as pio
    except ImportError:
        return "Plotly is not installed. Run: pip install plotly"

    try:
        spec: dict[str, Any] = json.loads(chart_spec_json)
        chart_type = spec.get("chart_type", "bar").lower()
        title = spec.get("title", "Chart")
        x_label = spec.get("x_label", "")
        y_label = spec.get("y_label", "")
        series_list: list[dict] = spec.get("data", [])
        layout_overrides: dict = spec.get("layout", {})
        orientation = spec.get("orientation", "v")

        _SUPPORTED = {"bar", "line", "scatter", "pie", "heatmap", "box", "area", "waterfall"}
        if chart_type not in _SUPPORTED:
            return (
                f"Unsupported chart_type '{chart_type}'. "
                f"Supported types: {', '.join(sorted(_SUPPORTED))}."
            )

        traces: list[go.BaseTraceType] = []

        for series in series_list:
            name = series.get("name", "")
            x = series.get("x", [])
            y = series.get("y", [])
            error_y = series.get("error_y")

            if chart_type == "bar":
                traces.append(go.Bar(
                    x=x, y=y, name=name,
                    orientation=orientation,
                    error_y=error_y,
                ))

            elif chart_type == "line":
                traces.append(go.Scatter(
                    x=x, y=y, name=name,
                    mode="lines+markers",
                    error_y=error_y,
                ))

            elif chart_type == "area":
                traces.append(go.Scatter(
                    x=x, y=y, name=name,
                    mode="lines",
                    fill="tozeroy",
                    error_y=error_y,
                ))

            elif chart_type == "scatter":
                traces.append(go.Scatter(
                    x=x, y=y, name=name,
                    mode="markers",
                    marker=series.get("marker", {}),
                    error_y=error_y,
                ))

            elif chart_type == "pie":
                traces.append(go.Pie(
                    labels=series.get("labels", x),
                    values=series.get("values", y),
                    name=name,
                    hole=series.get("hole", 0),
                ))

            elif chart_type == "heatmap":
                traces.append(go.Heatmap(
                    x=x,
                    y=y,
                    z=series.get("z", []),
                    colorscale=series.get("colorscale", "Blues"),
                ))

            elif chart_type == "box":
                traces.append(go.Box(
                    y=y, name=name,
                    boxpoints=series.get("boxpoints", "outliers"),
                ))

            elif chart_type == "waterfall":
                traces.append(go.Waterfall(
                    x=x, y=y, name=name,
                    measure=series.get("measure", ["relative"] * len(y)),
                ))

        layout = go.Layout(
            title={"text": title, "x": 0.5, "xanchor": "center"},
            xaxis={"title": x_label},
            yaxis={"title": y_label},
            template="plotly_white",
            **layout_overrides,
        )

        fig = go.Figure(data=traces, layout=layout)
        plotly_json = pio.to_json(fig)

        result: dict[str, str] = {"plotly_json": plotly_json}

        # PNG export — optional, requires kaleido
        try:
            png_bytes = fig.to_image(format="png", width=900, height=500, scale=2)
            result["png_base64"] = "data:image/png;base64," + base64.b64encode(png_bytes).decode()
        except Exception:
            pass  # kaleido not installed — omit png_base64

        return json.dumps(result)

    except (json.JSONDecodeError, TypeError) as exc:
        return f"Data format error: {exc}"
    except Exception as exc:
        logger.warning("Plotly chart creation failed: %s", exc)
        return f"Chart error: {exc}"


