@tool
def create_bar_chart(data_json: str, title: str = "Chart", output_path: str = "chart.png") -> str:
    """
    Create a bar chart from labelled data and return it as a base64 data URI.

    Args:
        data_json: JSON string with 'labels' and 'values' lists.
                   Example: '{"labels": ["A", "B"], "values": [10, 20]}'
        title: Chart title.
        output_path: Unused (kept for API compatibility).

    Returns:
        A base64-encoded data URI string (data:image/png;base64,...) that can be
        embedded directly into markdown as ![chart](<returned_value>).
    """
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt

        data: dict[str, Any] = json.loads(data_json)
        labels = data.get("labels", [])
        values = data.get("values", [])

        if not labels or not values or len(labels) != len(values):
            return "Error: 'labels' and 'values' must be non-empty lists of equal length."

        fig, ax = plt.subplots(figsize=(10, 6))
        bars = ax.bar(labels, values, color="steelblue", edgecolor="white")
        ax.set_title(title, fontsize=14, fontweight="bold")
        ax.set_ylabel("Value")
        ax.bar_label(bars, fmt="%.2f", padding=3)
        plt.tight_layout()

        buf = io.BytesIO()
        plt.savefig(buf, format="png", dpi=150)
        plt.close()
        buf.seek(0)
        b64 = base64.b64encode(buf.read()).decode("utf-8")
        return f"data:image/png;base64,{b64}"

    except (json.JSONDecodeError, KeyError) as exc:
        return f"Data format error: {exc}"
    except Exception as exc:
        logger.warning("Chart creation failed: %s", exc)
        return f"Chart error: {exc}"


@tool
def create_forest_plot(
    studies_json: str,
    outcome_label: str = "Effect Size (95% CI)",
    output_path: str = "forest_plot.png",
) -> str:
    """
    Create a forest plot visualising effect sizes and confidence intervals, and return it
    as a base64 data URI ready for embedding in markdown.

    Use this to present meta-analytic or multi-study evidence visually, following
    standard medical reporting conventions. Ideal for statistical_summary data where
    each outcome has a mean effect and 95% CI.

    Args:
        studies_json: JSON string — a list of objects, each with:
            - label (str): outcome name or study name (e.g. "HbA1c reduction")
            - effect_size (float): point estimate (mean difference, OR, RR, etc.)
            - ci_lower (float): lower bound of 95% confidence interval
            - ci_upper (float): upper bound of 95% confidence interval
            Example: '[{"label":"HbA1c reduction","effect_size":-0.5,"ci_lower":-0.7,"ci_upper":-0.3}]'

        outcome_label: X-axis label describing the outcome metric.
        output_path: Unused (kept for API compatibility).

    Returns:
        A base64-encoded data URI string (data:image/png;base64,...) — use this value
        directly as the chart_path in generate_report_from_template context JSON.
    """
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt

        studies: list[dict[str, Any]] = json.loads(studies_json)
        if not studies:
            return "Error: No studies provided for forest plot."

        labels = [s.get("label", f"Study {i+1}") for i, s in enumerate(studies)]
        effects = [float(s["effect_size"]) for s in studies]
        ci_lowers = [float(s["ci_lower"]) for s in studies]
        ci_uppers = [float(s["ci_upper"]) for s in studies]

        n = len(studies)
        fig, ax = plt.subplots(figsize=(11, max(4, n * 0.8 + 2.5)))

        y_positions = list(range(n - 1, -1, -1))  # top-to-bottom

        colors = ["#2563eb", "#16a34a", "#dc2626", "#9333ea", "#ea580c", "#0891b2"]
        for i, (y, eff, lo, hi) in enumerate(zip(y_positions, effects, ci_lowers, ci_uppers)):
            color = colors[i % len(colors)]
            # CI bar
            ax.plot([lo, hi], [y, y], color=color, linewidth=2.5, alpha=0.7, solid_capstyle="round")
            # Whisker caps
            ax.plot([lo, lo], [y - 0.15, y + 0.15], color=color, linewidth=2)
            ax.plot([hi, hi], [y - 0.15, y + 0.15], color=color, linewidth=2)
            # Effect square
            ax.plot(eff, y, "D", color=color, markersize=9, zorder=5)
            # Annotate effect value
            ax.annotate(
                f"{eff:+.2f} [{lo:+.2f}, {hi:+.2f}]",
                xy=(hi, y),
                xytext=(6, 0),
                textcoords="offset points",
                fontsize=8.5,
                va="center",
                color="#374151",
            )

        # Reference line at 0
        ax.axvline(0, color="#6b7280", linestyle="--", linewidth=1.2, alpha=0.8)

        ax.set_yticks(y_positions)
        ax.set_yticklabels(labels, fontsize=10)
        ax.set_xlabel(outcome_label, fontsize=11)
        ax.set_title("Summary of Findings — Forest Plot", fontsize=13, fontweight="bold", pad=12)
        ax.spines["top"].set_visible(False)
        ax.spines["right"].set_visible(False)
        ax.grid(axis="x", alpha=0.3, linestyle=":")

        # Extend x-axis right to leave room for annotations
        xmin, xmax = ax.get_xlim()
        ax.set_xlim(xmin, xmax + (xmax - xmin) * 0.35)

        plt.tight_layout()

        buf = io.BytesIO()
        plt.savefig(buf, format="png", dpi=150, bbox_inches="tight")
        plt.close()
        buf.seek(0)
        b64 = base64.b64encode(buf.read()).decode("utf-8")
        return f"data:image/png;base64,{b64}"

    except (json.JSONDecodeError, KeyError, ValueError) as exc:
        return f"Data format error: {exc}"
    except Exception as exc:
        logger.warning("Forest plot creation failed: %s", exc)
        return f"Forest plot error: {exc}"


