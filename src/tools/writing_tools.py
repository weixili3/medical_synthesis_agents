@tool
def generate_report_from_template(context_json: str) -> str:
    """
    Render a structured clinical/research report using the built-in Jinja2 template.

    Args:
        context_json: JSON string with keys:
            - title (str)
            - content_type (str)
            - executive_summary (str)          — includes clinical recommendations summary
            - introduction (str)
            - methodology (str)
            - prisma_flow (str, optional)      — PRISMA study-selection narrative
            - key_findings (list[str])
            - intervention_analysis (str, optional) — categorised by intervention type
            - evidence_analysis (str)
            - evidence_quality (str)
            - evidence_grade (str, optional)   — GRADE A/B/C/D
            - bias_assessment (str, optional)  — low/moderate/high/unclear
            - statistical_summary (dict, optional)
            - chart_path (str, optional)       — path to a generated chart/forest-plot PNG
            - discussion (str)
            - conclusions (str)
            - clinical_recommendations (str)   — actionable recommendations for clinicians
            - limitations (str)
            - citations (list[str])

    Returns:
        Rendered markdown report string.
    """
    try:
        ctx = json.loads(context_json)
        ctx.setdefault("date", datetime.now().strftime("%B %d, %Y"))
        ctx.setdefault("statistical_summary", {})
        ctx.setdefault("citations", [])
        ctx.setdefault("prisma_flow", "")
        ctx.setdefault("intervention_analysis", "")
        ctx.setdefault("evidence_grade", "")
        ctx.setdefault("bias_assessment", "")
        ctx.setdefault("chart_path", "")
        ctx.setdefault("clinical_recommendations", "")

        env = Environment(loader=BaseLoader())
        tmpl = env.from_string(REPORT_TEMPLATE)
        return tmpl.render(**ctx)

    except (json.JSONDecodeError, TypeError) as exc:
        return f"Template rendering error (bad JSON): {exc}"
    except Exception as exc:
        logger.warning("Report generation failed: %s", exc)
        return f"Report generation error: {exc}"


