@tool
def format_citation(source_json: str, style: str = "APA") -> str:
    """
    Format a source reference in the requested citation style.

    Args:
        source_json: JSON string with source metadata:
            - authors (list[str])
            - year (str | int)
            - title (str)
            - journal (str, optional)
            - volume (str, optional)
            - issue (str, optional)
            - pages (str, optional)
            - url (str, optional)
            - doi (str, optional)
        style: Citation style — 'APA', 'MLA', or 'Vancouver' (default 'APA').

    Returns:
        Formatted citation string.
    """
    try:
        src = json.loads(source_json)
    except (json.JSONDecodeError, TypeError) as exc:
        return f"Invalid source JSON: {exc}"

    authors = src.get("authors", ["Unknown Author"])
    year = src.get("year", "n.d.")
    title = src.get("title", "Untitled")
    journal = src.get("journal", "")
    volume = src.get("volume", "")
    issue = src.get("issue", "")
    pages = src.get("pages", "")
    url = src.get("url", "")
    doi = src.get("doi", "")

    style = style.upper()

    if style == "APA":
        author_str = _apa_authors(authors)
        citation = f"{author_str} ({year}). {title}."
        if journal:
            citation += f" *{journal}*"
            if volume:
                citation += f", *{volume}*"
                if issue:
                    citation += f"({issue})"
            if pages:
                citation += f", {pages}"
            citation += "."
        if doi:
            citation += f" https://doi.org/{doi}"
        elif url:
            citation += f" Retrieved from {url}"
        return citation

    elif style == "MLA":
        author_str = _mla_authors(authors)
        citation = f'{author_str} "{title}."'
        if journal:
            citation += f" *{journal}*"
            if volume:
                citation += f", vol. {volume}"
            if issue:
                citation += f", no. {issue}"
        citation += f", {year}"
        if pages:
            citation += f", pp. {pages}"
        citation += "."
        if url:
            citation += f" {url}."
        return citation

    elif style == "VANCOUVER":
        author_str = ", ".join(a.split(",")[0] if "," in a else a.split()[-1] for a in authors[:6])
        citation = f"{author_str}. {title}."
        if journal:
            citation += f" {journal}. {year}"
            if volume:
                citation += f";{volume}"
            if issue:
                citation += f"({issue})"
            if pages:
                citation += f":{pages}"
            citation += "."
        if doi:
            citation += f" doi:{doi}"
        return citation

    else:
        return f"Unsupported citation style '{style}'. Use APA, MLA, or Vancouver."


def _apa_authors(authors: list[str]) -> str:
    if not authors:
        return "Unknown Author"
    if len(authors) == 1:
        return authors[0]
    if len(authors) <= 7:
        return ", ".join(authors[:-1]) + ", & " + authors[-1]
    return ", ".join(authors[:6]) + ", ... " + authors[-1]


def _mla_authors(authors: list[str]) -> str:
    if not authors:
        return "Unknown Author."
    if len(authors) == 1:
        return authors[0] + "."
    return authors[0] + ", et al."


