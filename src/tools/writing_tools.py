@tool
def extract_markdown_section(markdown_text: str, section_title: str) -> str:
    """
    Extract a specific section from a markdown document by its heading.

    Args:
        markdown_text: Full markdown document text.
        section_title: The heading title to extract (case-insensitive).

    Returns:
        The content of the section, or an empty string if not found.
    """
    pattern = rf"(?:^|\n)#{{1,3}}\s+{re.escape(section_title)}\s*\n(.*?)(?=\n#{{1,3}}\s|\Z)"
    match = re.search(pattern, markdown_text, re.IGNORECASE | re.DOTALL)
    return match.group(1).strip() if match else ""


